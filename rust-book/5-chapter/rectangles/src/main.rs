// Refactoring with structs
#[derive(Debug)]
struct Rectangle {
    width: u32,
    height: u32,
}

impl Rectangle {
    fn area(&self) -> u32 {
        self.width * self.height
    }

    fn can_hold(&self, other: &Rectangle) -> bool {
        self.width > other.width && self.height > other.height
    }

    // Associated function.
    // Used for constructors that will return a new instance of the struct.
    fn square(size: u32) -> Self {
        Self {
            width: size,
            height: size,
        }
    }
}

fn main() {
    let rect1 = Rectangle {
        width: 30,
        height: 50,
    };
    let rect2 = Rectangle {
        width: 10,
        height: 40,
    };
    let rect3 = Rectangle {
        width: 60,
        height: 45,
    };

    println!(
        "The area of the 1st rectangle is {} square pixels.",
        rect1.area()
    );
    println!(
        "The area of the 2nd rectangle is {} square pixels.",
        rect2.area()
    );
    println!(
        "The area of the 3rd rectangle is {} square pixels.",
        rect3.area()
    );

    println!("Can rect1 hold rect2? {}", rect1.can_hold(&rect2));
    println!("Can rect1 hold rect3? {}", rect1.can_hold(&rect3));

    let sq = Rectangle::square(3);
    println!("sq: {:?}", sq);
    println!("The area of the square is {} square pixels.", sq.area());
}
